public class ContaCorrente {
    double limite;
    double taxa;
    public ContaCorrente() {
    }


    public double getLimite() {
        return limite;
    }
    public void setLimite(double limite) {
        this.limite = limite;
    }
    public double getTaxa() {
        return taxa;
    }
    public void setTaxa(double taxa) {
        this.taxa = taxa;
    }

}
